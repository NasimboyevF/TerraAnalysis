// ============================================================
// models/landSampleModel.js — Схема образца земли
//
// Образец земли — это набор измеренных показателей для
// конкретного земельного участка (координаты + данные замера).
// По образцу вычисляется итоговый индекс деградации.
// ============================================================

const mongoose = require('mongoose');

const landSampleSchema = new mongoose.Schema(
  {
    // Ссылка на пользователя которому принадлежит этот образец
    // ref: 'User' говорит Mongoose с какой моделью связывать
    userId: {
      type: mongoose.Schema.Types.ObjectId,
      ref: 'User',
      required: [true, 'Не указан владелец образца'],
    },

    // Название участка — для удобной идентификации
    title: {
      type: String,
      required: [true, 'Укажите название участка'],
      trim: true,
      maxlength: [100, 'Название не может быть длиннее 100 символов'],
    },

    // Описание участка (опциональное)
    description: {
      type: String,
      trim: true,
      maxlength: [500, 'Описание не может быть длиннее 500 символов'],
    },

    // Географические координаты участка
    // Используются для отображения на карте (Leaflet)
    coordinates: {
      lat: {
        type: Number,
        required: [true, 'Укажите широту'],
        min: [-90, 'Широта должна быть от -90 до 90'],
        max: [90, 'Широта должна быть от -90 до 90'],
      },
      lng: {
        type: Number,
        required: [true, 'Укажите долготу'],
        min: [-180, 'Долгота должна быть от -180 до 180'],
        max: [180, 'Долгота должна быть от -180 до 180'],
      },
    },

    // ─────────────────────────────────────────
    // ПОКАЗАТЕЛИ СОСТОЯНИЯ ЗЕМЛИ
    // ─────────────────────────────────────────

    // NDVI (Normalized Difference Vegetation Index) — индекс вегетации
    // Измеряется спутниковыми снимками или дроном
    // Диапазон: от -1 до 1
    //   < 0    — вода, снег, голый камень
    //   0–0.2  — голая почва, пустыня
    //   0.2–0.5 — редкая растительность, деградированные земли
    //   0.5–1  — здоровая растительность
    ndvi: {
      type: Number,
      required: [true, 'Укажите индекс NDVI'],
      min: [-1, 'NDVI должен быть от -1 до 1'],
      max: [1, 'NDVI должен быть от -1 до 1'],
    },

    // Влажность почвы в процентах (0–100%)
    // Норма для большинства культур: 20–40%
    // Слишком низкая (< 10%) — засуха, слишком высокая (> 70%) — заболачивание
    soilMoisture: {
      type: Number,
      required: [true, 'Укажите влажность почвы'],
      min: [0, 'Влажность не может быть отрицательной'],
      max: [100, 'Влажность не может быть больше 100%'],
    },

    // Индекс качества почвы (0–100)
    // Комплексный показатель: структура, pH, наличие питательных веществ
    // 0–30  — критически деградированная почва
    // 30–60 — умеренно деградированная
    // 60–80 — удовлетворительная
    // 80–100 — хорошая почва
    soilQualityIndex: {
      type: Number,
      required: [true, 'Укажите индекс качества почвы'],
      min: [0, 'Индекс качества от 0 до 100'],
      max: [100, 'Индекс качества от 0 до 100'],
    },

    // Содержание органического вещества в почве (%)
    // Норма для здоровой почвы: 3–5%
    // < 1% — критически мало, почва деградирована
    // > 5% — отличный показатель
    organicMatter: {
      type: Number,
      required: [true, 'Укажите содержание органического вещества'],
      min: [0, 'Значение не может быть отрицательным'],
      max: [20, 'Значение слишком большое (макс. 20%)'],
    },

    // Дата когда был сделан замер на местности
    sampledAt: {
      type: Date,
      required: [true, 'Укажите дату замера'],
      default: Date.now,
    },
  },
  {
    timestamps: true, // Автоматически добавляет createdAt и updatedAt
  }
);

// Индекс для быстрого поиска образцов конкретного пользователя
// Без индекса MongoDB перебирает все документы при каждом запросе
landSampleSchema.index({ userId: 1, createdAt: -1 });

module.exports = mongoose.model('LandSample', landSampleSchema);
